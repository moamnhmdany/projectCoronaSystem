package Models;

/**
 *
 * @author Lenovo
 */
public class StationInformations {
  private String stationInformationName;
  private String stationInformationNumber;
  private String stationInformationAddress ;

    public String getStationInformationName() {
        return stationInformationName;
    }

    public void setStationInformationName(String stationInformationName) {
        this.stationInformationName = stationInformationName;
    }

    public String getStationInformationNumber() {
        return stationInformationNumber;
    }

    public void setStationInformationNumber(String stationInformationNumber) {
        this.stationInformationNumber = stationInformationNumber;
    }

    public String getStationInformationAddress() {
        return stationInformationAddress;
    }

    public void setStationInformationAddress(String stationInformationAddress) {
        this.stationInformationAddress = stationInformationAddress;
    }
}
